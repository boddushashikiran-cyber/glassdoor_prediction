"""#### Imputation techniques used:
- **Median imputation** for numeric features (`Rating`, `size_encoded`, `company_age`): Median is robust to outliers and appropriate since these distributions are slightly skewed.
- **Mode imputation** for categorical features (`Sector`, `Type of ownership`, `state`): The most frequent category is a reasonable default for nominal variables without a natural mean.

### 2. Handling Outliers
"""

# Handling Outliers – IQR capping on avg_salary
Q1 = model_df['avg_salary'].quantile(0.25)
Q3 = model_df['avg_salary'].quantile(0.75)
IQR = Q3 - Q1
lower = Q1 - 1.5 * IQR
upper = Q3 + 1.5 * IQR

